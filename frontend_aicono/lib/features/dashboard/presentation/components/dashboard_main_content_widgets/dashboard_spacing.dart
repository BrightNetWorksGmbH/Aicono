/// Consistent spacing constants for the dashboard main content UI.
class DashboardSpacing {
  DashboardSpacing._();

  static const double block = 24.0; // Between main content blocks
  static const double section = 24.0; // Between sections within a block
  static const double content = 16.0; // After header, before content
  static const double titleSubtitle = 8.0; // Between title and subtitle
  static const double tight = 4.0; // Value to label in metric cards
  static const double cardGap = 12.0; // Between cards, list items
}
